# salvar como operacoes.py
def soma(n1,n2):
    resultado = n1 + n2
    print(resultado)
    
def subtrair(n1,n2):
    resultado = n1 - n2
    print(resultado)

def dividir(n1,n2):
    resultado = n1/n2
    print(resultado)
    
def multiplicar(n1,n2):
    resultado = n1*n2
    print(resultado)
    
soma(10,5)
subtrair(10,5)
dividir(10,5)
multiplicar(10,5)
