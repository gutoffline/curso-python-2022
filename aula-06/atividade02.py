def soma(n1,n2):
    total = float(n1) + float(n2)
    print(total)

def subtrair(n1, n2):
    total = float(n1) - float(n2)
    print(total)

def dividir(n1, n2):
    total = float(n1) / float(n2)
    print(total)

def multiplicar(n1, n2):
    total = float(n1) * float(n2)
    print(total)

num1 = input("Informe um número: ")
num2 = input("Informe outro número: ")
soma(num1,num2)
subtrair(num1,num2)
dividir(num1, num2)
multiplicar(num1, num2)
